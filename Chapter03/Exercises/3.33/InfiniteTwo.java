// Create an infinite loop showing the multiples of 2
// @Author: Adam Janecka
package pkg3_33;

public class InfiniteTwo {

    public static void main(String[] args) {
        
        int counter = 1; // used for while loop
        
        while (counter > 0)
        {
            System.out.printf("%d%n", 2 * counter);
            counter++;
        } // end while loop
    } // end method main
} // end class InfiniteTwo
