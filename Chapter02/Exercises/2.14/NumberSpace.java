// Displays the numbers 1 through 4 
// in three different programming methods
package pkg2_14;

public class NumberSpace {
    public static void main(String[] args) {
        System.out.println("1 2 3 4"); //Statement with one println statements
        
        System.out.print("1 "); // Statement with four print statements
        System.out.print("2 ");
        System.out.print("3 ");
        System.out.print("4 \n");
        
        System.out.printf("%s%n", "1 2 3 4"); // Statement with one printf statement
    } // end method main
} // end class NumberSpace
